void drawMyBoard(char board[10][10]){
    char l = 'A';
    
    for(int i=0; i<2; i++){
        for (int j=0; j<12; j++){
            if(j==0){
                if(i==0) cout << "    ";
                else cout << "___";
            }
            else if (j<11){
                if(i==0){
                    cout << l << " ";
                    l++;
                }
                else cout << "__";
            }
            else
                if(i==1) cout << "_";
            
        }
        cout << endl;
    }
    
    for(int i=0; i<10; i++){
        for(int j=0; j<12; j++){
            
            if(j==0){
                cout << i;
                if (i<10) cout << " ";
                cout << "| ";
            }

            else if(j<11){
                cout << board[i][j-1] << " ";
            }
            
            else cout << "|";
            
        }
        cout << endl;
    }
    for(int i=1; i<25; i++) cout << "_";
    cout << endl;
}